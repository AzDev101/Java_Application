//if statement
public class IfStatement{
	public static void main (String [] args){
		int num = 50;
		
		if(num > 25)
			System.out.println(num + " is greater than 25");
		
//if...else statement
		
		if(num > 25)
			System.out.println(num + " is greater than 50");
		
		else 
			System.out.println(num + " is less than 100");
	}
}